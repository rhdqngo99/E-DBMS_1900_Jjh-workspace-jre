-- 주석(comment)
-- ctrl + /
-- 주석이란, 컴퓨터가 개발자가 작성한 코드를 읽을 때
-- 무시하는 부분을 가리킨다.
-- 주석의 용도
-- 1. 개발자 간에 소통을 위해 적어놓는 글
-- 2. 소스코드의 실행을 임시로 막기 위해

-- 자료형(data type)
-- 데이터를 저장할 때 같은 형식의 값들을
-- 묶기 위한 일종의 카테고리를 지정하는 것이다.
-- 데이터를 저장할 때 적절한 자료형을 지정하면
-- 더 효율적인 데이터 관리가 가능하다.

-- 자료형의 종류
-- 숫자형
   -- NUMBER : 정수, 소수 모두 표현
   -- INTEGER: 정수만 저장
-- 문자형
   -- CHAR(n) : 고정길이 문자열
   -- VARCHAR2(n): 가변길이 문자열
-- 날짜, 시간
   -- DATE : 연,월,일,시,분,초를 표현
   -- TIMESTAMP: DATE보다 더 정밀한 시간 표현